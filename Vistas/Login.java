    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        //Capturamos valores del usuarios
        String userName = txtUserName.getText();
        String password = txtPassword.getText();
        // Mostramos valores
        System.out.println("Username: " + userName + ", Password: " + password);
        //Validamos campos vacios
        if (userName.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Hay campos sin diligenciar", "Informacion", JOptionPane.WARNING_MESSAGE);
        } else {
            if (userName.equals("MisionTIC") && password.equals("Ciclo2")) {
                JOptionPane.showMessageDialog(null, "Bienvenido al Sistema Mision TIC", "Bienvenido", WIDTH);
                //Hacer visible el nuevo Jframe
                UserMenu usermenu = new UserMenu();
                usermenu.setVisible(true);
                //Cerramos el frame Login
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Error al iniciar sesi칩n", "Error", JOptionPane.WARNING_MESSAGE);
            }
        }


    }//GEN-LAST:event_btnLoginActionPerformed

